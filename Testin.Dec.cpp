/* test on increment (++) and decrement(--) operator */
#include<iostream>
using namespace std;
int main(){
  int mark=76;
  cout<<mark<<endl;
  mark++;
  cout<<mark<<endl;
  ++mark;
  cout<<mark<<endl;
  mark=mark+1;
  cout<<mark<<endl;
  mark--;
  cout<<mark<<endl;
  --mark;
  cout<<mark<<endl;
  mark=mark-1;
  cout<<mark<<endl;
  return 0;
}